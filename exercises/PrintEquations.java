public class PrintEquations{
    public static void main(String[] args) {
        System.out.print("125 + 24 = ");
        System.out.println(125 + 24);
        System.out.print("125 - 24 = ");
        System.out.println(125 - 24);
        System.out.print("125 x 24 = ");
        System.out.println(125 * 24);
        System.out.print("125 / 24 = ");
        System.out.println(125 / 24);
        System.out.print("125 mod 24 = ");
        System.out.println(125 % 24);
    }
}